// Написати програму що обчислює піднесення до степеня. 
// В 1й промпт вводиться число,а в 2й промпт степінь. 
// Умови: Степінь може бути додатня, від'ємна або 0. 
// Обрахувати всі можливі варіанти циклом for. 
// Задачу бажано виконати використовуючи 1 цикл! 
// a^b = a*a b раз;
// a^-b= 1/a^b;
// a^0 = 1;

let d = prompt('Введіть число')
let c = prompt('Введіть степінь')
let k = 1;
let m = c;
if(c<0){
    m=-c;
    k=1/k;
}
for(let i=0; i<m; i++) {
    k=k*d;
}
document.write(`<h3> ${'Відповідь:' +k} </h3>`);
document.write(`<h2>Кінець циклу</h2>`);
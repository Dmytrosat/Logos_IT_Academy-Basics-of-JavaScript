//task 1
//  Створіть програму, яка обчислює факторіал натурального числа n, 
//  яке користувач введе з клавіатури.

let n = prompt("Введіть натуральне число");
let total = 1;
for(i = n; i > 1; i--){
total*= i;
}
document.write(`<h3> ${total} </h3>`)
document.write(`<h2>Кінець циклу</h2>`);

